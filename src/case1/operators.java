package case1;

public class operators {
	public static void main(String arg[]) {
		int a=8;
		switch(a)
		{
		case 1:System.out.println("man");
		break;
		case 2:System.out.println("men");
		break;
		case 8:System.out.println("child");
		break;
		default:System.out.println("default");
		break;
		
		}
	}

}
